#include<iostream>
using namespace std;
int main(){
    int a=4,b=8;
    cout<<"BITWISE AND OF "<<a<<" AND " <<b<<" IS : "<<(a&b)<<endl;
    cout<<"BITWISE OR OF "<<a<<" AND " <<b<<" IS : "<<(a|b)<<endl;
    cout<<"BITWISE XOR OF "<<a<<" AND " <<b<<" IS : "<<(a^b)<<endl;
    cout<<"BITWISE LEFT SHIFT OF "<<a<<" AND " <<b<<" IS : "<<(4<<1)<<endl;
    cout<<"BITWISE RIGHT SHIFT OF "<<a<<" AND " <<b<<" IS : "<<(10>>1)<<endl;
    

return 0;
}